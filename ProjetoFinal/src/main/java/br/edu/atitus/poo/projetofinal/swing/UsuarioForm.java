    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        List<Usuario> usuarios = usuarioNegocio.findAll();
        DefaultTableModel tabela = (DefaultTableModel) tblUsuarios.getModel();
        tabela.setRowCount(0);
        for (int i = 0; i < usuarios.size(); i++){
            tabela.addRow(new Object[]{
                usuarios.get(i).getId(),
                usuarios.get(i).getNome(),
                usuarios.get(i).getDepartamento()
            });
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String idDigitado = txtId.getText();
       String nomeDigitado = txtNome.getText();
       Departamento departamento = (Departamento) cmbDepartamento.getSelectedItem();
       
        Usuario usuarioNew = new Usuario();
        if (!idDigitado.isEmpty())
            usuarioNew.setId(Integer.parseInt(idDigitado));
        usuarioNew.setNome(nomeDigitado);
        usuarioNew.setDepartamento(departamento);
        
        usuarioNegocio.save(usuarioNew);
    }//GEN-LAST:event_jButton1ActionPerformed


